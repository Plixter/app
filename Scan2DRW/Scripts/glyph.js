import $ from 'jquery';

var GlyphManager = (function() {

    function loadData(data) {
        for (var i = 0; i < data.length; i++) {
            glyphs.push(data[i]);
        }
    }

    function init(tContainer) {

        var t, tb;
        $tContainer = $(tContainer);

        // populate the table body <tbody>
        tb = $('<tbody></tbody>');
        $.each(glyphs, function(i, l) {
            t = l.split(/\s*,\s*/);
            tb.append($('<tr></tr>')
                .append($('<td></td>').addClass("xrx-glyphicon").addClass(t[0]))
                .append($('<td>' + t[1] + '</td>'))
                .append($('<td>' + t[2] + '</td>'))
                .append($('<td>' + t[3] + '</td>')));
        });

        $tContainer.append(tb);
    }

    var glyphs = [],
        $tContainer,

        publicAPI = {
            loadData: loadData,
            init: init
        };

    return publicAPI;

})();

